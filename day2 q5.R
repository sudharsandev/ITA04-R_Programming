# Create three vectors
numeric_vector <- c(1, 2, 3, 4, 5)
character_vector <- c("apple", "banana", "orange", "grape", "kiwi")
logical_vector <- c(TRUE, FALSE, TRUE, TRUE, FALSE)

# Display content of each vector and their type
cat("Numeric Vector:\n")
print(numeric_vector)
cat("Type: ", typeof(numeric_vector), "\n\n")

cat("Character Vector:\n")
print(character_vector)
cat("Type: ", typeof(character_vector), "\n\n")

cat("Logical Vector:\n")
print(logical_vector)
cat("Type: ", typeof(logical_vector), "\n")
